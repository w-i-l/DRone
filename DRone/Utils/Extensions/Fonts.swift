import SwiftUI


extension Font {
    static func abel(size: CGFloat) -> Font {
        .custom("Abel-Regular", size: size)
    }
    
    static func asket(size: CGFloat) -> Font {
        .custom("asket.narrow-light", size: size)
    }
}
